#------------------single range argument---------------
print('My name is')
for i in range(5):
    print('name is five', str(i))

#----------------print 1+1+2+ .....+100------------

total = 0
for num in range(101):
    total = total + num
print(total)

#-------------while loop---------------------------

print('My name')
i = 0
while i<5:
    print('name is five', str(i))
    i = i+1

# -------------------- range function with 2 arguments------
print('Enter name')
for i in range(12,18):
    print('name is printed', str(i))

# -------------------- range function with 3 arguments --------
print('Enter name')
for i in range(10,100,5):
    print('name is printed', str(i))

